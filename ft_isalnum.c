int	ft_isalnum(int word)
{
	if ('a' >= word && word <= 'z')
		return (1);
	else if ('A' >= word && word <= 'Z')
		return (1);
	else if ('0' >= word && word <= '9')
		return (1);
	else
		return (0);
}
